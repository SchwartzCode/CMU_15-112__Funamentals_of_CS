def patternedMessage(msg, patt):
    #loop through message and replace all non-whitespace or newline characters with message

    #remove whitespace from message
    msg = msg.replace(" ", "")

    #current position in string msg
    pos = 0
    #use this to avoid index out of range error
    maxPos = len(msg)

    for i in range(len(patt)):
        if (patt[i] != " " and patt[i] != "\n"):
            patt = patt[:i] + msg[pos] + patt[i+1:]
            pos += 1
            if pos == maxPos:
                pos=0

    # removing intial or trailing whitespace
    while(patt[0] == "\n"):
        patt = patt[1:]
    while(patt[-1] == "\n"):
        patt = patt[:-1]
    print(patt)


